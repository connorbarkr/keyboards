void keyboard_post_init_user(void) {
	rgblight_sethsv(HSV_GOLDENROD);
}

bool encoder_update_user(uint8_t index, bool clockwise) {
	if (index == 0) {
        // Volume control
        if (clockwise) {
            tap_code(KC_VOLD);
        } else {
            tap_code(KC_VOLU);
        }
    } else if (index == 1) {
        // Page up/Page down
        if (clockwise) {
            tap_code(KC_PGUP);
        } else {
            tap_code(KC_PGDN);
        }
    }
	return true;
}